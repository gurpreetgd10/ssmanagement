    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // TODO add your handling code here:
        Connection conn=null;
        PreparedStatement p=null;
        String catname,subname,prodname;
        int fcq,ccq,dq,stock;
        try{
            conn=configwarehouseadmin.getConnection1();
            if(conn.isValid(1)){
                catname=jTextField1.getText();
                subname=jTextField2.getText();
                prodname=jTextField3.getText();
                fcq=Integer.parseInt(jTextField4.getText());
                ccq=Integer.parseInt(jTextField5.getText());
                dq=Integer.parseInt(jTextField6.getText());
                stock=Integer.parseInt(jTextField7.getText());
                
                p=conn.prepareStatement("insert into PRODUCT values(?,?,?,?,?,?,?,?,?)");
                p.setString(1, "Warehouse");
                p.setString(2, id);
                p.setString(3, catname);
                p.setString(4, subname);
                p.setString(5, prodname);
                p.setInt(6, fcq);
                p.setInt(7, ccq);
                p.setInt(8, dq);
                p.setInt(9, stock);
                if(p.executeUpdate()==1){
                    JOptionPane.showMessageDialog(rootPane, "Product Added!");
                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField3.setText("");
                    jTextField4.setText("");
                    jTextField5.setText("");
                    jTextField6.setText("");
                    jTextField7.setText("");
                    
                }
                else{
                    JOptionPane.showMessageDialog(rootPane, "Couldn't Add!");
                }
            }
            conn.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }                                        

